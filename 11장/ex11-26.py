BASE = 1444   # 60갑자 기준연도는 1444년(세종 26년)임
ten = ('갑', '을', '병', '정', '무', '기', '경', '신', '임', '계')
twelve = ('자', '축', '인', '묘', '진', '사', '오', '미', '신', '유', '술', '해')
animal = ('쥐', '소', '호랑이', '토끼', '용', '뱀', '말', '양', '원숭이', '닭', '개', '돼지')

year = int(input("연도 입력 : "))
i1 = (year - BASE) % 10
i2 =  (year - BASE) % 12
print("%d년은 %s년(%s의 해) 입니다." % (year, ten[i1]+twelve[i2], animal[i2]))
